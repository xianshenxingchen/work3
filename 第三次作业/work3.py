import pandas as pd

# 1. 定义准确的列名列表（根据题目图片）
col_names = [
    '交易类型', '交易时间', '交易卡号', '刷卡类型', '线路号',
    '车辆编号', '上车站点', '下车站点', '驾驶员编号', '运营公司编号'
]

# 2. 读取数据
# 关键点：
# - sep=',' : 根据你提供的数据样本，必须用逗号
# - names=col_names : 强制指定列名
# - header=0 : 告诉pandas原文件的第一行是表头，读取时跳过它（防止“交易时间”这四个字变成数据）
print("--- 步骤 1: 读取数据 ---")
try:
    df = pd.read_csv('ICData.csv', sep=',', names=col_names, header=0)

    # 验证读取是否成功：打印列名和前几行
    print("识别到的列名：", df.columns.tolist())
    print(df.head())

    # ==========================================
    # 3. 时间解析 (现在应该可以正常运行了)
    # ==========================================
    print("\n--- 步骤 2: 时间解析 ---")

    # 这里的错误是因为上一行数据里混入了“交易时间”这个字符串，现在修复了读取方式，这里就不会报错了
    df['交易时间'] = pd.to_datetime(df['交易时间'])

    # 提取小时
    df['hour'] = df['交易时间'].dt.hour

    print("时间解析成功，前5行数据：")
    print(df[['交易时间', 'hour']].head())

except Exception as e:
    print(f"发生错误: {e}")